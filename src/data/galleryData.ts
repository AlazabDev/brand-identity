// Generate Oracle storage URLs (001-389, skip broken extensions)
function generateOracleUrls(): string[] {
  const urls: string[] = [];
  for (let i = 1; i <= 389; i++) {
    const num = String(i).padStart(3, "0");
    // Skip known non-image files
    if (i === 57 || i === 78 || i === 79) continue;
    urls.push(`${ORACLE_BASE}${num}.jpg`);
  }
  return urls;
}

